console.log("Conversao de Tipos");

// concatena caracter
console.log("ano" + 2021);
console.log("2" + "2");

// transforma tipo
console.log(parseInt("2") + parseInt("2"));

// converte automatico
console.log("10" / "2"); // = 5
console.log("7" / "2"); // = 3.5

// nao consegue converte ai da NaN - Not a Number
console.log("Ricardo" / "2"); // NaN